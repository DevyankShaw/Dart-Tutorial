void main(){
  //Built in data types
  //var automatically takes up the preffered value
  //int
  int age =20;
  var age1 = 20;
  print("age = $age");
  print("age = $age1");
  //we can use hexvalue also but make sure we have 8 char after x
  var hexValue = 0XABCDECDE;
  print("hexValue = $hexValue");
  //double
  double value= 20.0;
  var value2 = 30.4;
  var exponents = 1.42e2;
 
  print("exponents = $exponents"); 
  print("value = $value");
  print("value2 = $value2");
  
  //String
  String name = "Henry";
  var name2 = "Matt";
  print("name = $name");
  print("name1 = $name2");
  
  //boolean
  bool isValid = true;
  bool isValid1 = false;
  print("isValid = $isValid");
  print("isValid1 = $isValid1");
}
